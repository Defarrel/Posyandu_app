Color getColorStatusGizi(String status) {
  if (status.contains("Buruk") ||
      status.contains("Obesitas") ||
      status.contains("Sangat Pendek") ||
      status.contains("Sangat Kurus")) {
    return Colors.red;
  }
  if (status.contains("Kurang") ||
      status.contains("Risiko") ||
      status.contains("Pendek") ||
      status.contains("Kurus")) {
    return Colors.orange;
  }
  if (status.contains("Normal") || status.contains("Baik")) {
    return Colors.green;
  }
  if (status.contains("Tinggi")) return Colors.blue;
  return Colors.grey;
}

String getKategoriStatusGizi(double berat, String gender, int umurBulan) {
  final umurCheck = umurBulan > 60 ? 60 : umurBulan;
  final standards = (gender.toUpperCase().contains('L'))
      ? _boysWeightAge[umurCheck]
      : _girlsWeightAge[umurCheck];

  if (standards == null) return "Data Tidak Tersedia";

  final sdMin3 = standards[0];
  final sdMin2 = standards[1];
  final sdPlus2 = standards[2];
  final sdPlus3 = standards[3];

  if (berat < sdMin3) return "Gizi Buruk (Merah)";
  if (berat >= sdMin3 && berat < sdMin2) return "Gizi Kurang (Kuning)";
  if (berat >= sdMin2 && berat <= sdPlus2) return "Gizi Normal (Hijau)";
  if (berat > sdPlus2 && berat <= sdPlus3) return "Risiko Gizi Lebih (Kuning)";
  return "Obesitas (Merah)";
}

String getStatusTinggiUmur(double tinggi, String gender, int umurBulan) {
  final umurCheck = umurBulan > 60 ? 60 : umurBulan;
  final standards = (gender.toUpperCase().contains('L'))
      ? _boysHeightAge[umurCheck]
      : _girlsHeightAge[umurCheck];

  if (standards == null) return "Data Tidak Tersedia";

  final sdMin3 = standards[0];
  final sdMin2 = standards[1];
  final sdPlus3 = standards[2];

  if (tinggi < sdMin3) return "Sangat Pendek";
  if (tinggi >= sdMin3 && tinggi < sdMin2) return "Pendek";
  if (tinggi >= sdMin2 && tinggi <= sdPlus3) return "Normal";
  return "Tinggi";
}

String getStatusBeratTinggi(
  double berat,
  double tinggi,
  String gender,
  int umurBulan,
) {
  int tinggiRounded = tinggi.round();

  if (tinggiRounded < 45) tinggiRounded = 45;
  if (tinggiRounded > 120) tinggiRounded = 120;

  final standards = (gender.toUpperCase().contains('L'))
      ? _boysWeightHeight[tinggiRounded]
      : _girlsWeightHeight[tinggiRounded];

  if (standards == null) return "Data Tidak Tersedia";

  final sdMin3 = standards[0];
  final sdMin2 = standards[1];
  final sdPlus1 = standards[2];
  final sdPlus2 = standards[3];
  final sdPlus3 = standards[4];

  if (berat < sdMin3) return "Sangat Kurus";
  if (berat >= sdMin3 && berat < sdMin2) return "Kurus";
  if (berat >= sdMin2 && berat <= sdPlus1) return "Normal";
  if (berat > sdPlus1 && berat <= sdPlus2) return "Risiko Gizi Lebih";
  if (berat > sdPlus2 && berat <= sdPlus3) return "Gizi Lebih";
  return "Obesitas";
}

